"""

ㅇImplicit Type Conversion
Python automatically converts one data type to another data type. This process doesn't need any user involvement.

ㅇExplicit Type Conversion
Users convert the data type of an object to required data type.

"""


""" Implicit Type Conversion """

print(type(123+1.23))
# <class 'float'>


""" Explicit Type Conversion """

print(type(int('123')))
# <class 'int'>
