print(*zip((1, 2, 3), (10, 20, 30), (100, 200, 300)), sep="\n")
# (1, 10, 100)
# (2, 20, 200)
# (3, 30, 300)

print(*[(1, 2, 3), (10, 20, 30), (100, 200, 300)], sep="\n")
# (1, 2, 3)
# (10, 20, 30)
# (100, 200, 300)

print([*range(1, 4)], [*range(10, 40, 10)], *range(100, 400, 100), sep="\n")
# [1, 2, 3]
# [10, 20, 30]
